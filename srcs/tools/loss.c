#include "ft_ping.h"

uint8_t calc_packet_loss(t_ping *ping)
{
    // 3 = 10 - 7
    // 3 / 10 = 0.3
    // 0.3 % 100 = 30 %
    return (((ping->sequence - ping->received) / ping->sequence) * 100);
}
